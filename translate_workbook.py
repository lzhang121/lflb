SHEET_NAME_MAP = {
    "00_概要": "00_概览",
    "01_要件トレース": "01_需求追踪",
    "02_権限マトリクス": "02_权限矩阵",
    "03_出力項目マッピング": "03_输出项映射",
    "04_テストケース": "04_测试用例",
    "05_業務シナリオ": "05_业务场景",
    "06_LOG検証": "06_LOG验证",
    "07_境界値_異常系": "07_边界值_异常系",
}


def translate_text(text: str) -> str:
    original = text
    for src, dst in REPLACEMENTS:
        text = text.replace(src, dst)
    for src, dst in TERMS:
        text = text.replace(src, dst)
    text = text.replace("　", " ")
    return text if text else original


def main():
    if len(sys.argv) < 3:
        raise SystemExit("Usage: python translate_workbook.py <input.xlsx> <output.xlsx>")
    input_path = Path(sys.argv[1])
    output_path = Path(sys.argv[2])
    wb = load_workbook(input_path)

    for ws in wb.worksheets:
        if ws.title in SHEET_NAME_MAP:
            ws.title = SHEET_NAME_MAP[ws.title]

    for ws in wb.worksheets:
        for row in ws.iter_rows():
            for cell in row:
                if isinstance(cell.value, str) and cell.value and not cell.value.startswith("="):
                    cell.value = translate_text(cell.value)
                elif isinstance(cell.value, str) and cell.value.startswith("="):
                    for old, new in SHEET_NAME_MAP.items():
                        cell.value = cell.value.replace(f"'{old}'", f"'{new}'")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    wb.save(output_path)


if __name__ == "__main__":
    main()
